CREATE TABLE `CellPlans` (
    `Id` INT(10) NOT NULL AUTO_INCREMENT,
    `Title` VARCHAR(255) NOT NULL,
    `TitleIsValid` BOOLEAN,
    `TitleComment` VARCHAR(255),
    `Local` VARCHAR(255) NOT NULL,
    `LocalIsValid` BOOLEAN,
    `LocalComment` VARCHAR(255),
    `Date` DATE NOT NULL,
    `DateIsValid` BOOLEAN,
    `DateComment` VARCHAR(255),
    `Duration` TIME NOT NULL,
    `DurationIsValid` BOOLEAN,
    `DurationComment` VARCHAR(255),
    `Mode` VARCHAR(255) NOT NULL,
    `ModeIsValid` BOOLEAN,
    `ModeComment` VARCHAR(255),
    `Synopsis` VARCHAR(255) NOT NULL,
    `SynopsisIsValid` BOOLEAN,
    `SynopsisComment` VARCHAR(255),
    `Justification` VARCHAR(255) NOT NULL,
    `JustificationIsValid` BOOLEAN,
    `JustificationComment` VARCHAR(255),
    `TargetAudience` VARCHAR(255) NOT NULL,
    `TargetAudienceIsValid` BOOLEAN,
    `TargetAudienceComment` VARCHAR(255),
    `Activities` VARCHAR(255) NOT NULL,
    `ActivitiesIsValid` BOOLEAN,
    `ActivitiesComment` VARCHAR(255),
    `Tools` VARCHAR(255) NOT NULL,
    `ToolsIsValid` BOOLEAN,
    `ToolsComment` VARCHAR(255),
    `ResultIndicators` VARCHAR(255) NOT NULL,
    `ResultIndicatorsIsValid` BOOLEAN,
    `ResultIndicatorsComment` VARCHAR(255),
    `MeansOfVerification` VARCHAR(255) NOT NULL,
    `MeansOfVerificationIsValid` BOOLEAN,
    `MeansOfVerificationComment` VARCHAR(255),
    PRIMARY KEY (`Id`) USING BTREE
);